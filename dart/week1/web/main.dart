void main() {
  var score = 80;

  if (score >= 80) {
    print("Grade A");
  } else if (score >= 75) {
    print("Grade B+");
  } else if (score >= 70) {
    print("Grade B");
  } else if (score >= 65) {
    print("Grade C+");
  } else if (score >= 60) {
    print("Grade C");
  } else if (score >= 55) {
    print("Grade D+");
  } else if (score >= 50) {
    print("Grade D");
  } else {
    print("Grade F");
  }
}
